func TestAnalysis(t *testing.T) {
	bi, ok := debug.ReadBuildInfo()
	if !ok {
		t.Fatal("failed to read build info")
	}
	module := bi.Main.Path + "/..."

	pkgs, err := packages.Load(&packages.Config{
		Mode:    packages.LoadAllSyntax,
		Context: t.Context(),
	}, module)
	if err != nil {
		t.Fatalf("load packages %v: %v", module, err)
	}

	g, err := checker.Analyze([]*analysis.Analyzer{
		appends.Analyzer,
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		buildssa.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		ctrlflow.Analyzer,
		deepequalerrors.Analyzer,
		defers.Analyzer,
		directive.Analyzer,
		errorsas.Analyzer,
		// fieldalignment.Analyzer,
		findcall.Analyzer,
		framepointer.Analyzer,
		httpmux.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		inspect.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		pkgfact.Analyzer,
		printf.Analyzer,
		reflectvaluecompare.Analyzer,
		shadow.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		slog.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stdversion.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		testinggoroutine.Analyzer,
		tests.Analyzer,
		timeformat.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		unusedwrite.Analyzer,
		usesgenerics.Analyzer,
		waitgroup.Analyzer,
	},
		pkgs,
		&checker.Options{
			SanityCheck: true,
		})
	if err != nil {
		t.Fatalf("run analyzers: %v", err)
	}

	for act := range g.All() {
		if !strings.HasPrefix(act.Package.PkgPath, bi.Main.Path) {
			continue
		}
		name := fmt.Sprintf("%s/%s", act.Analyzer, act.Package)

		generated := make(map[string]bool)

		t.Run(name, func(t *testing.T) {
			if act.Err != nil {
				t.Error(act.Err)
			}

			for _, diag := range act.Diagnostics {
				pos := act.Package.Fset.Position(diag.Pos)

				if _, ok := generated[pos.Filename]; !ok {
					b, err := os.ReadFile(pos.Filename)
					if err != nil {
						t.Errorf("read file %v: %v", pos.Filename, err)
						continue
					}
					generated[pos.Filename] = generatedRe.Match(b)
				}
				if generated[pos.Filename] {
					continue
				}

				t.Errorf("[%v] %s", pos, diag.Message)
			}
		})
	}
}
